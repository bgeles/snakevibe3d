const FALLBACK_LANG = "en-US";

export function detectLanguage() {
  const nav = navigator.language || FALLBACK_LANG;
  if (LANGUAGES[nav]) return nav;
  if (nav.startsWith("pt")) return "pt-BR";
  if (nav.startsWith("es")) return "es-ES";
  return FALLBACK_LANG;
}

export function getSupportedLanguages() {
  return Object.keys(LANGUAGES);
}

export function t(lang, key, vars = {}) {
  const table = LANGUAGES[lang] || LANGUAGES[FALLBACK_LANG];
  const value = table[key] || LANGUAGES[FALLBACK_LANG][key] || key;
  return Object.entries(vars).reduce((acc, [k, v]) => acc.replace(`{${k}}`, String(v)), value);
}
